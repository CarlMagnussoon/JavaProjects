#Funktion som förflyttar huvudet.   
def movehead(head, tail, r, txt): 
    #Riktning och steg plockas ut ur textsträngen. 
    direction = txt[0]
    steps = int(txt[2:])
 
    #Flyttar huvudet 1 steg åt gången, itererar över antal steg som ska tas. 
    # Efter varje iteration kollar vi om svansen måste flyttas också 
