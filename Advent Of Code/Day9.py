#funktion som kollar om huvud och svans är grannar
def IsNeighbour(HCP, TCP): 
    rowdiff = HCP[0] - TCP[0]
    coldiff = HCP[1] - TCP[1] 
    
    if abs(rowdiff) <= 1 and abs(coldiff) == 0: 
        return 1
    elif abs(coldiff) <= 1 and abs(rowdiff) == 0: 
        return 1
    elif abs(rowdiff) == 1 and abs(coldiff) == 1:
        return 1
    elif abs(rowdiff) == 0 and abs(coldiff) == 0:
        return 1
    else: 
        return 0

